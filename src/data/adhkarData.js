// Sunnah fasting days
export function getSunnahFastingDays(year, month) {
  // White Days (13th, 14th, 15th of each Hijri month)
  // Monday and Thursday of each week
  const days = [];
  const daysInMonth = new Date(year, month + 1, 0).getDate();

  for (let d = 1; d <= daysInMonth; d++) {
    const date = new Date(year, month, d);
    const dow = date.getDay();
    if (dow === 1) days.push({ date: d, type: 'monday', label: 'Monday Fast' });
    if (dow === 4) days.push({ date: d, type: 'thursday', label: 'Thursday Fast' });
  }
  return days;
}

